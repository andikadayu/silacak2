package com.example.silacak2.model;

public class YesterdayModel {
    String idYes,imageYes,nameYes,nrpYes;

    public YesterdayModel(String idYes, String imageYes, String nameYes, String nrpYes) {
        this.idYes = idYes;
        this.imageYes = imageYes;
        this.nameYes = nameYes;
        this.nrpYes = nrpYes;
    }

    public String getIdYes() {
        return idYes;
    }

    public String getImageYes() {
        return imageYes;
    }

    public String getNameYes() {
        return nameYes;
    }

    public String getNrpYes() {
        return nrpYes;
    }
}
